	id INTEGER GENERATED BY DEFAULT AS IDENTITY,
	integer_field INTEGER,
	PRIMARY KEY(id)
);
-- 2. Create a sequence (generator)
RECREATE SEQUENCE insert_auto_seq;

-- 3. Create a BEFORE INSERT trigger to auto-generate IDs
