CREATE TRIGGER insert_auto_trigger FOR insert_auto
ACTIVE BEFORE INSERT POSITION 0
AS
BEGIN
    IF (NEW.id IS NULL) THEN
        NEW.id = NEXT VALUE FOR insert_auto_seq;
END;
